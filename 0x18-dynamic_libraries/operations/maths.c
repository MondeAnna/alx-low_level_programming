#include "main.h"

/**
 * add - add
 * @a: left operand
 * @b: left operand
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub
 * @a: left operand
 * @b: left operand
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul
 * @a: left operand
 * @b: left operand
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div
 * @a: left operand
 * @b: left operand
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod
 * @a: left operand
 * @b: left operand
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
