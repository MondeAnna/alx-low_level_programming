/**
 * _memset - fill memory with constant byte
 * @s: memory area
 * @b: byte
 * @n: memory area size
 *
 * Description: call within `main` in order for
 * exercise to work
 *
 * Return: pointer to memory area (char *)
 */
char *_memset(char *s, char b, unsigned int n)
{
	while (n--)
		s[n] = b;
	return (s);
}

/**
 * p - print buffer space
 * @b: buffer
 * @n: buffer size
 *
 * Return: void
 */
void p(char *b, unsigned int n)
{
	unsigned int i;

	i = 0;

	while (i < n)
	{
		if (i % 10)
			putchar(' ');

		if (!(i % 10) && i)
			putchar('\n');

		printf("0x%.2x", b[i]);

		i++;
	}

	putchar('\n');
}

/**
 * exec - testing entry point
 *
 * Description: written as to allow the use of
 * a minimal `main` function during testing
 * without destroying "testing" code
 *
 * Return: void
 */
void exec(void)
{
	char b[98] = {0x00};

	putchar('\n');
	p(b, 98);

	_memset(b, 0x01, 98);

	putchar('\n');
	p(b, 98);
}
